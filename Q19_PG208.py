# to calculate perimeter and area of a parallelogram
l=float(input("enter length of the parallelogram:"))
w=float(input("enter width of the parallelogram:"))
h=float(input("enter height of the parallelogram:"))
area=l*h
perimeter=2*l+2*w
print("the area of the given parallelogram is:",area)
print("the perimeter of the given parallelogram is:",perimeter)
